# List is []

# Creating a Python list with different data types
a = [10, 20, "GfG", 40, True]

print(a)

# Accessing elements using indexing
print(a[0])  # 10
print(a[1])  # 20
print(a[2])  # "GfG"
print(a[3])  # 40
print(a[4])  # True
print(a[1:3]) #20 , "GFG"

# Checking types of elements
print(type(a[2]))  # str
print(type(a[4]))  # bool




